package java_basic;
// Write a function to print your name and call the function from main method.
public class calling_function {

    static void myMethod() {
        System.out.println("My Name is 'VEERASEKHAR'");
    }

    public static void main(String[] args) {
        /* To call a method, you just need to type the method name
           followed by open and closed parentheses */
        myMethod();
    }
	

}
